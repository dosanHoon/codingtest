import 거꾸로 from "./거꾸로";
import 거꾸로2 from "./거꾸로2";
import 문자열만들기 from "./문자열만들기";

test("거꾸로", () => {
  expect(거꾸로("거꾸로")).toBe("로꾸거");
  expect(거꾸로("안녕")).toBe("녕안");
});

test("거꾸로2", () => {
  expect(거꾸로2("1 2 3 4 5")).toBe("5 4 3 2 1");
  expect(거꾸로2("2 4 6 7 8")).toBe("8 7 6 4 2");
});

test("문자열만들기", () => {
  expect(
    문자열만들기("안녕하세요. 저는 제주대학교 컴퓨터공학전공 혜림입니다.")
  ).toBe(5);
  expect(문자열만들기("안녕하세요. 쓰레기")).toBe(2);
});
